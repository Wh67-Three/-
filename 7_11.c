void f(char str[], char ch)
{
	int i, j;
	for(i=j=0;str[i]!='\0';i++)
		if(str[i]!=ch)
			str[j++]=str[i];
	str[j]='\0';
}
/*特别注意i，j的变化
变量声明：

int i, j;：声明两个整数变量 i 和 j。i 用于遍历原始字符串 str，j 用于追踪新字符串的位置。
初始化：

for (i = j = 0; str[i] != '\0'; i++)：初始化 i 和 j 为 0，并开始遍历字符串 str，直到遇到字符串结束符 '\0'。
遍历和删除字符：

if (str[i] != ch)：检查当前字符 str[i] 是否不等于要删除的字符 ch。
str[j++] = str[i];：如果当前字符 str[i] 不等于 ch，则将其复制到新位置 str[j]，并将 j 增加 1 以准备存储下一个字符。
结束字符串：

str[j] = '\0';：在循环结束后，将新字符串的末尾设置为字符串结束符 '\0'，确保字符串正确结束。
*/
